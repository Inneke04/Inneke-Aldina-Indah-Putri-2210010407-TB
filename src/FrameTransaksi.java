    private void tambahTransaksi() {
if (txtKodePelanggan.getText().isEmpty() || txtKodeMobil.getText().isEmpty() || jDateChooser1.getDate() == null || jDateChooser2.getDate() == null || txtLamaSewa.getText().isEmpty() || txtTotal.getText().isEmpty()) {
    JOptionPane.showMessageDialog(this, "Semua field harus diisi!", "Peringatan", JOptionPane.WARNING_MESSAGE);
    return;
}
        try {
            String query = "INSERT INTO transaksi (kode_pelanggan, kode_mobil, tgl_sewa, tgl_kembali, lama_sewa, total) VALUES (?, ?, ?, ?, ?, ?)";
            PreparedStatement ps = conn.prepareStatement(query);
            ps.setString(1, txtKodePelanggan.getText());
            ps.setString(2, txtKodeMobil.getText());
            ps.setString(3, new SimpleDateFormat("yyyy-MM-dd").format(jDateChooser1.getDate()));
            ps.setString(4, new SimpleDateFormat("yyyy-MM-dd").format(jDateChooser2.getDate()));
            ps.setString(5, txtLamaSewa.getText());
            ps.setString(6, txtTotal.getText());
            ps.executeUpdate();
            JOptionPane.showMessageDialog(this, "Transaksi berhasil ditambahkan");
            loadTable();
            clearForm();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal menambahkan transaksi: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void updateTransaksi() {
        int selectedRow = tblTransaksi.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Pilih transaksi yang akan diubah", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }
        if (txtKodePelanggan.getText().isEmpty() || txtKodeMobil.getText().isEmpty() || jDateChooser1.getDate() == null || jDateChooser2.getDate() == null || txtLamaSewa.getText().isEmpty() || txtTotal.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Semua field harus diisi!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }
        try {
            String query = "UPDATE transaksi SET kode_pelanggan = ?, kode_mobil = ?, tgl_sewa = ?, tgl_kembali = ?, lama_sewa = ?, total = ? WHERE id_transaksi = ?";
            PreparedStatement ps = conn.prepareStatement(query);
            ps.setString(1, txtKodePelanggan.getText());
            ps.setString(2, txtKodeMobil.getText());
            ps.setString(3, new SimpleDateFormat("yyyy-MM-dd").format(jDateChooser1.getDate()));
            ps.setString(4, new SimpleDateFormat("yyyy-MM-dd").format(jDateChooser2.getDate()));
            ps.setString(5, txtLamaSewa.getText());
            ps.setString(6, txtTotal.getText());
            ps.setInt(7, Integer.parseInt(tblTransaksi.getValueAt(selectedRow, 0).toString()));
            ps.executeUpdate();
            JOptionPane.showMessageDialog(this, "Transaksi berhasil diubah");
            loadTable();
            clearForm();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal mengubah transaksi: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void hapusTransaksi() {
        int selectedRow = tblTransaksi.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Pilih transaksi yang akan dihapus", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }
        try {
            String query = "DELETE FROM transaksi WHERE id_transaksi = ?";
            PreparedStatement ps = conn.prepareStatement(query);
            ps.setInt(1, Integer.parseInt(tblTransaksi.getValueAt(selectedRow, 0).toString()));
            ps.executeUpdate();
            JOptionPane.showMessageDialog(this, "Transaksi berhasil dihapus");
            loadTable();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal menghapus transaksi: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void cariTransaksi() {
        String keyword = txtCari.getText();
        if (keyword.isEmpty()) {
            loadTable();
            return;
        }
        try {
            String query = "SELECT * FROM transaksi WHERE kode_pelanggan LIKE '%" + keyword + "%' OR kode_mobil LIKE '%" + keyword + "%'";
            Statement stmt = conn.createStatement();
            ResultSet rs = stmt.executeQuery(query);
            DefaultTableModel model = (DefaultTableModel) tblTransaksi.getModel();
            model.setRowCount(0); // Reset tabel
            while (rs.next()) {
                Object[] row = {rs.getString("id_transaksi"), rs.getString("kode_pelanggan"), rs.getString("kode_mobil"), rs.getString("tgl_sewa"), rs.getString("tgl_kembali"), rs.getString("lama_sewa"), rs.getString("total")};
                model.addRow(row);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal melakukan pencarian: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    
