    private void loadTable() {
       DefaultTableModel model = (DefaultTableModel) tblTransaksi.getModel();
    model.setRowCount(0); // Reset tabel
    try {
        String query = "SELECT * FROM transaksi";
        Statement stmt = conn.createStatement();
        ResultSet rs = stmt.executeQuery(query);
        while (rs.next()) {
            Object[] row = {rs.getString("id_transaksi"), rs.getString("kode_pelanggan"), rs.getString("kode_mobil"), rs.getString("tgl_sewa"), rs.getString("tgl_kembali"), rs.getString("lama_sewa"), rs.getString("total")};
            model.addRow(row);
        }
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Gagal memuat data transaksi: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
    }

