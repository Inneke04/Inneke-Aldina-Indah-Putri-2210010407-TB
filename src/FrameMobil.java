    private void loadTable() {
        DefaultTableModel model = (DefaultTableModel) tblMobil.getModel();
        model.setRowCount(0); // Reset tabel
        try {
            String query = "SELECT * FROM mobil";
            Statement stmt = conn.createStatement();
            ResultSet rs = stmt.executeQuery(query);
            while (rs.next()) {
                Object[] row = {rs.getInt("id_mobil"),rs.getString("kode_mobil"), rs.getString("brand"), rs.getString("model"), rs.getString("nopol"), rs.getDouble("harga"), rs.getString("status")};
                model.addRow(row);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal memuat data mobil: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

