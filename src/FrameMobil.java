    private void tambahMobil() {
        if (txtKodeMobil.getText().isEmpty() || txtBrand.getText().isEmpty() || txtModel.getText().isEmpty() || txtNoPolisi.getText().isEmpty() || txtHarga.getText().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Semua field harus diisi!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }
        try {
            String query = "INSERT INTO mobil (kode_mobil, brand, model, nopol, harga, status) VALUES (?, ?, ?, ?, ?, ?)";
            PreparedStatement ps = conn.prepareStatement(query);
            ps.setString(1, txtKodeMobil.getText());
            ps.setString(2, txtBrand.getText());
            ps.setString(3, txtModel.getText());
            ps.setString(4, txtNoPolisi.getText());
            ps.setDouble(5, Double.parseDouble(txtHarga.getText()));
            ps.setString(6, cmbStatus.getSelectedItem().toString());
            ps.executeUpdate();
            JOptionPane.showMessageDialog(this, "Mobil berhasil ditambahkan");
            loadTable();
            clearForm();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal menambahkan mobil: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void updateMobil() {
         int selectedRow = tblMobil.getSelectedRow();
    if (selectedRow == -1) {
        JOptionPane.showMessageDialog(this, "Pilih mobil yang akan diubah", "Peringatan", JOptionPane.WARNING_MESSAGE);
        return;
    }
    if (txtKodeMobil.getText().isEmpty() || txtBrand.getText().isEmpty() || txtModel.getText().isEmpty() || txtNoPolisi.getText().isEmpty() || txtHarga.getText().isEmpty()) {
        JOptionPane.showMessageDialog(this, "Semua field harus diisi!", "Peringatan", JOptionPane.WARNING_MESSAGE);
        return;
    }
    try (PreparedStatement ps = conn.prepareStatement("UPDATE mobil SET kode_mobil = ?, brand = ?, model = ?, nopol = ?, harga = ?, status = ? WHERE id_mobil = ?")) {
        ps.setString(1, txtKodeMobil.getText());
        ps.setString(2, txtBrand.getText());
        ps.setString(3, txtModel.getText());
        ps.setString(4, txtNoPolisi.getText());
        ps.setDouble(5, Double.parseDouble(txtHarga.getText()));
        ps.setString(6, cmbStatus.getSelectedItem().toString());
        ps.setInt(7, Integer.parseInt(tblMobil.getValueAt(selectedRow, 0).toString()));
        ps.executeUpdate();
        JOptionPane.showMessageDialog(this, "Mobil berhasil diubah");
        loadTable();
        clearForm();
    } catch (SQLException e) {
        JOptionPane.showMessageDialog(this, "Gagal mengubah mobil: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
    }
    }

    private void hapusMobil() {
        int selectedRow = tblMobil.getSelectedRow();
        if (selectedRow == -1) {
            JOptionPane.showMessageDialog(this, "Pilih mobil yang akan dihapus", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }
        try {
            String query = "DELETE FROM mobil WHERE id_mobil = ?";
            PreparedStatement ps = conn.prepareStatement(query);
            ps.setString(1, tblMobil.getValueAt(selectedRow, 0).toString());
            ps.executeUpdate();
            JOptionPane.showMessageDialog(this, "Mobil berhasil dihapus");
            loadTable();
            clearForm();
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal menghapus mobil: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

    private void cariMobil() {
        String keyword = txtCari.getText();
        if (keyword.isEmpty()) {
            loadTable();
            return;
        }
        DefaultTableModel model = (DefaultTableModel) tblMobil.getModel();
        model.setRowCount(0); // Reset tabel
        try {
            String query = "SELECT * FROM mobil WHERE kode_mobil LIKE ?";
            PreparedStatement ps = conn.prepareStatement(query);
            ps.setString(1, "%" + keyword + "%");
            ResultSet rs = ps.executeQuery();
            while (rs.next()) {
                Object[] row = {rs.getInt("id_mobil"),rs.getString("kode_mobil"), rs.getString("brand"), rs.getString("model"), rs.getString("nopol"), rs.getDouble("harga"), rs.getString("status")};
                model.addRow(row);
            }
        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this, "Gagal mencari mobil: " + e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

    
